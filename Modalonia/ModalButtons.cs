namespace Modalonia
{
    /// <summary>
    /// Represent modal buttons enumeration.
    /// </summary>
    public enum ModalButtons
    {
        /// <summary>
        /// Show modal without buttons.
        /// </summary>
        None = 0,

        /// <summary>
        /// Show "YES" and "NO" button to the modal.
        /// </summary>
        YesNo = 1,

        /// <summary>
        /// Show "OK" button to the modal.
        /// </summary>
        Ok = 2,

        /// <summary>
        /// Show "OK" and "CANCEL" button to the modal.
        /// </summary>
        OkCancel = 3
    }
}
